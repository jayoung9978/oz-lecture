// 1씩 증가하는 while 반복문
let num = 1;
while (num <= 10) {
	console.log(num);
	num++; // 코드가 한번 반복될때 마다 num 변수를 1씩 증가시킨다.
}
console.log(num);

// 2씩 증가하는 while 반복문
let num2 = 1;
while (num2 <= 100) {
	console.log(num2);
	num2 += 2; // 코드가 한번 반복될때 마다 num 변수를 1씩 증가시킨다.
}
console.log(num2);
